package com.jmmunoz.dp06_command_design_pattern;

public class AC {

    public void turnOn() {
        System.out.println("AC is on");
    }

    public void turnOff() {
        System.out.println("AC is off");
    }

    public void decTemp() {
        System.out.println("Decreasing temperature by 1");
    }

    public void incTemp() {
        System.out.println("Increasing temperature by 1");
    }
}
